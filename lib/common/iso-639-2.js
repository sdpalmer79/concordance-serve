    function getLangObject(dataArray) {
        return {
            a3: dataArray[0],
            a3t: dataArray[1],
            a2: dataArray[2],
            name: dataArray[3],
            eng: dataArray[4],
            dir: dataArray[5]
        };
    }

    function getLangMap(isAlpha2Code) {
        let i, lang, len;

        if(!alpha2Map) {
            alpha2Map = {};
            alpha3Map = {};
            for(i=0, len=data.length; i<len; i++) {
                lang = getLangObject(data[i]);
                alpha3Map[lang.a3] = lang;
                if(lang.a2) {
                    alpha2Map[lang.a2] = lang;
                }
                data[i] = lang;
            }
        }

        return isAlpha2Code ? alpha2Map : alpha3Map;
    }

    function compareLanguages(a, b, sortBy) {
        var aVal, bVal;

        sortBy = sortBy || 'a3';
        aVal = a[sortBy];
        bVal = b[sortBy];

        return aVal < bVal ? -1 : aVal > bVal ? 1 : 0;
    }


    exports.getLanguage = function (languageCode, isAlpha2Code) {
        return getLangMap(isAlpha2Code)[languageCode];
    };

    exports.getLanguages = function (sortBy, isAlpha2Code) {
        let copy;

        sortBy = sortBy || 'a3';

        //initialize maps
        getLangMap();

        copy = data.slice();
        copy.sort(function(a,b) {
            return compareLanguages(a, b, sortBy);
        });

        return isAlpha2Code ? copy.filter(function(lang) { return !!lang.a2; }) : copy;
    };

